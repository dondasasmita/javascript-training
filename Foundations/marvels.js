//datatype - Array

const superHeroes = ['Iron Man', 'Spiderman', 'Captain America', 'Thor']

//access all elements in the array
console.log(superHeroes)

//access index 0 in the superHeroes array
console.log(superHeroes[0])

//access the last index
console.log(superHeroes[superHeroes.length - 1]);

//concatenate the integer to the String using ` and ${}
console.log(`We have ${superHeroes.length} heroes `);
